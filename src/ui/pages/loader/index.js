import { connect } from 'react-redux';

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

import LoaderComponent from '../../base/loader';

//------------------------------------------------------------------------------
//------------------------------------------------------------------------------
//------------------------------------------------------------------------------

export default connect ((state) => {
  let loader = state.loader.toJS();
  return {
    isLoading: loader.isPageLoading,
    text: loader.message
  };
})(LoaderComponent);